#Tipos de datos, Para saber el tipo de dato se escribe type antes del dato
#String
print('hola José')

#Integer 
print(30)

#Float
print(3.5)

#Boolean
True
False

#List en las listas los datos pueden cambiar y se escriben dentro de []
[10, 20, 30]
['hola', 'Chao']
[10, 'hola', True]

#Tuples en las tuple los datos no pueden cambiar y se escriben dentro de ()
(10, 20, 30)

# Diccionarios para especificar datos de una unidad por ejemplo una persona
{
  "nombre": "José",
  "apellido": "Molina",
  "apodo": "JGM"
}

# None es un tipo de dato sin definir 
None

